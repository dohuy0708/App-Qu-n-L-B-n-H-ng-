package com.example.jewelryecommerceapp.Models;

import java.io.Serializable;

public class Address  implements Serializable {
    private String province, district, ward, street;
    public String addressId,detail,phoneNumber,fullName,userId;

    public Address() {
    }

    public Address(String  userId,String fullName, String province, String district, String ward,String street, String detail, String phoneNumber) {
        this.fullName = fullName;
        this.userId=userId;
        this.street=street;
        this.province = province;
        this.district = district;
        this.ward = ward;
        this.detail = detail;
        this.phoneNumber = phoneNumber;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getWard() {
        return ward;
    }

    public void setWard(String ward) {
        this.ward = ward;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getAddressId() {
        return addressId;
    }

    public void setAddressId(String addressId) {
        this.addressId = addressId;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }
}
